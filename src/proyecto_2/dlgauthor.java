    private void authorselItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_authorselItemStateChanged
        // TODO add your handling code here:
        this.listauthor.removeAll();
        if(this.authorsel.getSelectedItem() != null && !"--Seleccionar--".equals(this.authorsel.getSelectedItem().toString()))
        {
            Hash Obj = new Hash();
            int hashcode = Obj.getHashCode(this.authorsel.getSelectedItem().toString());
            for(int i=0; i< Proyecto_2.Auth_Proj[hashcode].length; i++)
            {
                if(Proyecto_2.Auth_Proj[hashcode][i] != 0)
                {
                    int hashproj = Proyecto_2.Auth_Proj[hashcode][i];
                    String proj = Proyecto_2.Proj_Tit[hashproj];
                    this.listauthor.add(proj);
                }
                else
                {
                    break;
                }

            }
        }
    }//GEN-LAST:event_authorselItemStateChanged

