    private void btacceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btacceptActionPerformed
        // TODO add your handling code here:
        if (this.listkey.getSelectedItem() == null)
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una palabra clave de la lista.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            dlgsearchword anword = new dlgsearchword(Proyecto_2.interf, true);
            anword.keywordsel.setEnabled(false);
            anword.keywordsel.removeAllItems();
            anword.keywordsel.addItem(this.listkey.getSelectedItem());
            anword.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btacceptActionPerformed

