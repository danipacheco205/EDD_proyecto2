    public void filleachkey()
    {
        this.listkey.removeAll();
        String[] addkeys = Proyecto_2.arb_keywords.inorden(Proyecto_2.arb_keywords.getRaiz(), "").split(", ");
        for (int i=0; i<addkeys.length; i++)
        {
            this.listkey.add(addkeys[i]);
        }
    }
